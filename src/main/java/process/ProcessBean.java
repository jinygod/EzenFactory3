package process;

public class ProcessBean {
	
	public String process_idx;
	public String process_seq;
	public String process_name;
	public String process_info;
	public String process_time;
	public String process_timeunit;
	
	public String getProcess_idx() {
		return process_idx;
	}
	public void setProcess_idx(String process_idx) {
		this.process_idx = process_idx;
	}
	public String getProcess_seq() {
		return process_seq;
	}
	public void setProcess_seq(String process_seq) {
		this.process_seq = process_seq;
	}
	public String getProcess_name() {
		return process_name;
	}
	public void setProcess_name(String process_name) {
		this.process_name = process_name;
	}
	public String getProcess_info() {
		return process_info;
	}
	public void setProcess_info(String process_info) {
		this.process_info = process_info;
	}
	public String getProcess_time() {
		return process_time;
	}
	public void setProcess_time(String process_time) {
		this.process_time = process_time;
	}
	public String getProcess_timeunit() {
		return process_timeunit;
	}
	public void setProcess_timeunit(String process_timeunit) {
		this.process_timeunit = process_timeunit;
	}
	
}
